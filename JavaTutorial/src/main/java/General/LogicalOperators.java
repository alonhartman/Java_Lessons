package General;

public class LogicalOperators
{

	public static void main(String[] args)
	{
		int subject1 = 40;
		int subject2 = 60;
		//&& is AND			|| is OR
		if ((subject1 <= 35) && (subject2 >= 35))
		{
			System.out.println("the condition is true");
		}
		else
		{
			System.out.println("the condition is false");
		}
		
	}

}
